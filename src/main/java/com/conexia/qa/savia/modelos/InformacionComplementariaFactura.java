package com.conexia.qa.savia.modelos;

public class InformacionComplementariaFactura {

	private String codDiagnostico;
	private String aplicaPBS;
	private String centroDeCosto;
	private String altoCosto;
	private String aplicaNOPBS;

	public String getCodDiagnostico() {
		return codDiagnostico;
	}

	public void setCodDiagnostico(String codDiagnostico) {
		this.codDiagnostico = codDiagnostico;
	}

	public String getAplicaPBS() {
		return aplicaPBS;
	}

	public void setAplicaPBS(String aplicaPBS) {
		this.aplicaPBS = aplicaPBS;
	}

	public String getCentroDeCosto() {
		return centroDeCosto;
	}

	public void setCentroDeCosto(String centroDeCosto) {
		this.centroDeCosto = centroDeCosto;
	}

	public String getAltoCosto() {
		return altoCosto;
	}

	public void setAltoCosto(String altoCosto) {
		this.altoCosto = altoCosto;
	}

	public String getAplicaNOPBS() {
		return aplicaNOPBS;
	}

	public void setAplicaNOPBS(String aplicaNOPBS) {
		this.aplicaNOPBS = aplicaNOPBS;
	}
	
}
